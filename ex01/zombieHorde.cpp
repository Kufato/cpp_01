#include "Zombie.hpp"

Zombie*	zombieHorde(int N, std::string name) {
	if (N <= 0) {
		std::cout << _RED << "Invalid numbers of zombie" << _END << std::endl;
		return (NULL);
	}
	Zombie	*zombieHorde = new Zombie[N];

	for (int i = 0; i < N; i++)
		zombieHorde[i].setName(name);
	return (zombieHorde);
}